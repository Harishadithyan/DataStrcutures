class Move {
    public static void main(String[] args) {
        int[] arr = {1, 0, 3, 4, 5, 6, 0};
        int slow = 0;
        for (int fast = 0; fast < arr.length; fast++) {
            if (arr[fast] != 0) {
                int temp = arr[slow];
                arr[slow] = arr[fast];
                arr[fast] = temp;
                slow++;
            }
        }
        for (int x : arr) {
            System.out.print(x + " ");
        }
    }
}
